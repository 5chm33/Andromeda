import * as Module from './ragPipeline.js';

import { mkdtempSync, rmSync } from "fs";
import { tmpdir } from "os";
import { join } from "path";

// Isolate workspace to temp dir to avoid loading production memory files
const _tmpWs = mkdtempSync(join(tmpdir(), "andromeda-test-"));
process.env.ANDROMEDA_WORKSPACE = _tmpWs;


describe('ragPipeline', () => {

  beforeEach(() => {
    vi.clearAllMocks();
  });

  it('module loads without errors', () => {
    expect(Module).toBeDefined();
  });

  it('exports are defined', () => {
    expect(Module).toBeDefined();
    expect(typeof Module).toBe('object');
  });

  it('registerRagRoutes is defined', () => {
    expect(typeof Module.registerRagRoutes).toBe('function');
  });

  it('module has expected structure', () => {
    const keys = Object.keys(Module);
    expect(keys.length).toBeGreaterThanOrEqual(0);
  });

  it('no unexpected throws on import', () => {
    expect(Module).toBeTruthy();
  });

  it('exported types are correct', () => {
    for (const key of Object.keys(Module)) {
      const val = (Module as any)[key];
      expect(['function', 'object', 'string', 'number', 'boolean', 'undefined'].includes(typeof val)).toBe(true);
    }
  });

  it('chunkDocument is defined', () => {
    expect(typeof Module.chunkDocument).toBe('function');
  });

  it('chunkDocument returns an array for short content', () => {
    const result = Module.chunkDocument('hello world');
    expect(Array.isArray(result)).toBe(true);
  });

  it('chunkDocument handles empty string', () => {
    const result = Module.chunkDocument('');
    expect(Array.isArray(result)).toBe(true);
  });

  it('chunkDocument accepts source parameter', () => {
    const result = Module.chunkDocument('some content', 'test-source');
    expect(Array.isArray(result)).toBe(true);
  });

});
